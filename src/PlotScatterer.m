function PlotScatterer(param,domain)

H=param.H; L=param.L;

if strcmp(domain,'layers_rectangle')
    l1_l = [0,0; L/4, 0]; l1_m = [L/4, 0; 3*L/4, 0]; l1_r = [3*L/4, 0; L, 0];
    l2 = [0,-H/3; L, -H/3]; l3 = [0,-2*H/3; L, -2*H/3]; 
    l4 = [L/4, 0; L/4, 2*H/3];  l5 = [L/4, 2*H/3; 3*L/4, 2*H/3]; l6 = [3*L/4, 2*H/3; 3*L/4, 0];
    
    subplot(1,2,1)
    plot3(l1_l(:,1),l1_l(:,2),[2,2],'k--', l1_r(:,1),l1_r(:,2),[2,2],'k--', ...
        l5(:,1),l5(:,2),[2,2],'k--', l2(:,1),l2(:,2),[2,2],'k--', ...
        l3(:,1),l3(:,2),[2,2],'k--', l4(:,1),l4(:,2),[2,2],'k--', ...
        l6(:,1),l6(:,2),[2,2],'k--',l1_m(:,1),l1_m(:,2),[2,2],'k--','LineWidth',2), hold on
    view(2)
    
    subplot(1,2,2)
    plot3(l1_l(:,1),l1_l(:,2),[2,2],'k--', l1_r(:,1),l1_r(:,2),[2,2],'k--', ...
        l5(:,1),l5(:,2),[2,2],'k--', l2(:,1),l2(:,2),[2,2],'k--', ...
        l3(:,1),l3(:,2),[2,2],'k--', l4(:,1),l4(:,2),[2,2],'k--', ...
        l6(:,1),l6(:,2),[2,2],'k--',l1_m(:,1),l1_m(:,2),[2,2],'k--','LineWidth',2), hold on
    view(2)
elseif strcmp(domain,'u_shape')
    l1 = [0,-0.5*H; L/4, -0.5*H]; l2 = [L/4, -0.5*H; L/4, 0.5*H];
    l3 = [L/4, 0.5*H; 3*L/4, 0.5*H]; l4 = [3*L/4, 0.5*H; 3*L/4, -0.5*H]; 
    l5 = [3*L/4, -0.5*H; L, -0.5*H]; 

    subplot(1,2,1)
    plot3(l1(:,1),l1(:,2),[2,2],'k--', l2(:,1),l2(:,2),[2,2],'k--', ...
        l5(:,1),l5(:,2),[2,2],'k--',  l3(:,1),l3(:,2),[2,2],'k--', ...
        l4(:,1),l4(:,2),[2,2],'k--', 'LineWidth',2), hold on
    view(2)
    
    subplot(1,2,2)
    plot3(l1(:,1),l1(:,2),[2,2],'k--', l2(:,1),l2(:,2),[2,2],'k--', ...
        l5(:,1),l5(:,2),[2,2],'k--',  l3(:,1),l3(:,2),[2,2],'k--', ...
        l4(:,1),l4(:,2),[2,2],'k--', 'LineWidth',2), hold on
    view(2)
end


end